package baekjoon;

import java.util.*;

public class p26489 {
    //https://www.acmicpc.net/problem/26489
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int n = 0;
        while(scanner.hasNextLine()) {
            scanner.nextLine();
            n++;
        }
        System.out.println(n);
    }
}